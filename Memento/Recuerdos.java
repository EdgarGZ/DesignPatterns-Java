package mementofigura;

public class Recuerdos {
    private double altoGuardar;
    private double anchoGuardar;
    private double coorXGuardar;
    private double coorYGuardar;

    public Recuerdos(double altoGuardar, double anchoGuardar, double coorXGuardar, double coorYGuardar) {
        this.altoGuardar = altoGuardar;
        this.anchoGuardar = anchoGuardar;
        this.coorXGuardar = coorXGuardar;
        this.coorYGuardar = coorYGuardar;
    }

    public double getAltoGuardar() {
        return altoGuardar;
    }

    public double getAnchoGuardar() {
        return anchoGuardar;
    }

    public double getCoorXGuardar() {
        return coorXGuardar;
    }

    public double getCoorYGuardar() {
        return coorYGuardar;
    }
    

    
    
}
